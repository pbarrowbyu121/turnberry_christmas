        Sarah skates in circles around the shepherds and their flocks. Raising
        her hands, she says, stumbling over her hard-to-remember lines, “Fear
        not: for, behold, I bring you good tidings of great joy, which shall be
        to all people. For unto you is born this day in the city of David a
        Saviour, which is Christ the Lord.”
        <br /><br />
