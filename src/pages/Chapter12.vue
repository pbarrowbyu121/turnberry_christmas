      <p>
        Cut to the skating rink. Night. The skating rink is fully decorated. The
        Christmas lights shine through the cold night.
        <br /><br />
        Later that night, the whole town congregates at the skating rink to
        watch the nativity. But there’s a little less magic with Catie gone (the
        whole town misses her). There’s an area on the ice designated as the
        stage, although it’s not elevated. In front of it, rows of chairs have
        been set up for the town to sit in. A curtain has been hung across some
        rope to hide the scenery from the audience. Off to the side of the
        congregation is a brass band setting up to provide music for the night.
        We see Cassandra blowing air through her trumpet.
        <br /><br />
        Beryl and Erin sit in the audience and wait for the show to start, but
        there’s clearly some delay. The show isn’t starting. Madi approaches
        Erin and Beryl. She explains that they’re having problems convincing the
        Christmas Angel (Sarah) to go on with the show. Erin starts to get up to
        walk with Madi backstage to talk to Sarah. Beryl also starts to get up.
        “I’ve got this.” He motions for Erin to set back down. “Let me talk to
        her.” Erin gives him the go-ahead.
        <br /><br />
        Beryl and Madi walk backstage and find Sarah with a tear-streaked face
        and red eyes. She’s still wiping tears away with the back of her hand.
        Her lips are quivering. Sarah can barely go on, she’s almost sobbing
        with Catie gone. Beryl kneels down to be at her level. “What’s wrong,
        Angel?” He asks.
        <br /><br />
        “I miss Catie. I want her to come and see me be the angel and tell
        everyone about the baby Jesus!”
        <br /><br />
        “There were some things I wanted her to hear me say, too.” Beryl says.
        He hugs Sarah. Sarah sobs on Beryl’s shoulders. Beryl acknowledges that
        Catie was pretty great, and that the whole town misses her. But he
        explains there’s nothing they can really do about that right now. He
        says that it’s going to be Christmas soon, and Sarah needs to do her
        part. “I bet, wherever Catie is right now, she’ll be thinking about you
        when you tell everyone about the baby Jesus.” He’s trying to convince
        himself that she’ll be thinking about him, as he knows he’ll be thinking
        about her.
        <br /><br />
        “I loved her, Uncle Beryl!” Sarah sobs.
        <br /><br />
        Beryl chuckles, “Oh, Angel. That makes two of us.” Beryl pulls Sarah
        into his arms and gives her a big hug. He realizes that he should have
        told Catie how he felt.
        <br /><br />
        Sarah sobs on Beryl’s shoulders while they’re still hugging. Beryl talks
        Sarah into “doing it for Catie.” He wipes away her tears. Sarah has a
        smile on her face. “So whaddya say, Angel? Think you can get out there
        and be the best angel Christmas has ever seen?” Sarah nods, wiping away
        tears.
        <br /><br />
        Beryl sends Sarah on her way and walks back to his seat next to Erin.
        Erin looks at Beryl with a look that says “Well? Is everything okay?”
        Beryl nods and winks at Erin and gives the thumbs up. “Sarah’s going to
        be fine.” He starts to sit down
        <br /><br />
        “Are you?” questions Erin with raised eyebrows.
        <br /><br />
        Before he can sit down, Beryl thinks for a minute and says, “There’s
        something I gotta go do. Save my seat?” Erin nods with a big smile.
        Beryl squeezes between the seats in the next row and people’s legs in
        his row. “Excuse me. Sorry folks. Pardon me. Glad you all could make it.
        Sorry about this.” Finally he makes it out of the row and briskly walks
        to the skating rink’s exit. He almost runs into Kelly and Lauren.
        They’re no longer dressed in their coveralls. They’ve put on nice
        clothes for the show.
        <br /><br />
        “Oh good,” says Beryl with relief. “Is Catie still at the garage?”
        <br /><br />
        “No,” says Kelly with a confused look on his face. Then a thought comes
        to him. “But, they did mention that they still need to pack a few things
        at the Bed and Breakfast.” Before Kelly can finish, Beryl sprints down
        Main Street toward the Bed and Breakfast.
        <br /><br />
        The rink lights go low except for two spotlights that show Madi taking
        center stage. Madi thanks all those who helped. She says something funny
        and the crowd chuckles. She introduces the show. The curtains are pulled
        back revealing a beautifully decorated skating rink that now more
        closely resembles an ancient Bethlehem and countryside with ice instead
        of dirt for ground. Scattered “Ooh” and “Ahhh” emanate from the crowd.
      </p>
