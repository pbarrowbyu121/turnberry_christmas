        fidget in nervousness on her behalf. Sarah tries again. “And this shall
        be a sign unto you; ye shall find...ye shall find…” She pauses, trying
        to remember. The silence hangs heavy in the air. Sarah looks nervously
        to the audience.
        <br /><br />
        She tries again. “Ye shall find…” Her eyes get really big. A smile
        stretches across her face as she sees someone in the very back of the
        audience. “Ye shall find CATIE!”
        <br /><br />
        The whole audience/town gasps. They look to see Catie walking into the
        rink. Even the spotlights swirl around to shine on Catie in the back of
        the rink. She shields her eyes from the bright spotlights. The town
        begins to chatter with excitement at Catie’s return. Catie is a little
        embarrassed at all the attention. The audience stands up in their chairs
        and begin to walk toward her. The first ones to greet her are Emma,
        Brock, Chella, Dallan, Esther, Derek, Trent, Jenn. They take turns
        giving her a hug. Even Beryl and Erin move toward Catie, but Beryl hangs
        back, cautious to get too excited. Catie, however, walks straight toward
        Beryl. The crowd starts to part. They know where she’s headed. But Sarah
        intercepts, almost knocking Catie over. She gives Catie’s waist the
        biggest hug. “Catie!” Sarah squeals. Catie chuckles then lifts up Sarah
        into her arms.
        <br /><br />
        Erin comes to get Sarah off Catie. “Come here my little angel.”
        <br /><br />
        Catie hands Sarah over and gives Erin a sisterly hug. There’s still a
        low hum of people chattering over the return of Catie.
        <br /><br />
        Catie briskly walks up to Beryl. She stops in front of him like she has
        before. But this time she surprises him by wrapping her arms around him.
        Beryl is a little taken aback, but happy. They embrace. The whole town
        forms an impromptu circle around the two.
        <br /><br />
        “What made you come back?” Asks Beryl, still holding her tight, but
        looking intently into her eyes.
        <br /><br />
        She looks around at the crowd. She eyes Trent, who has a big smile on
        his face. She says, “I decided it was finally time to follow my
        heartpeanut.” Trent’s smile gets bigger and he squeezes Jenn closer. “I
        just couldn’t imagine Christmas anywhere else. With anyone else.” The
        crowd cheers and congratulates each other. The married couples hold each
        other close, some kiss. Children dressed in homemade ancient Hebrew
        costumes join their families.
        <br /><br />
        Not far in the distance the clock tower strikes twelve, heralding in the
        Holiday of Holidays. Snow begins to fall from the sky as if signaled by
        the bells. The crowd begins to exclaim “Oh,” in excitement. The band
        begins to play Hark the Herald Angels Sing at a quick tempo (not slow
        like it’s always played in church). The crowd cheers. Couples and
        families embrace one another and begin singing along at the top of their
        lungs. It’s Christmas!
        <br /><br />
        Beryl and Catie stare into each other’s eyes, each with a big grin and
        snow flakes in their hair and on their face. They kiss. The crowd sees
        this and cheers even wilder. It resembles New Year’s Eve more than
        Christmas morning. They break the kiss. Catie asks, with a smile
        (knowing the answer), “What are you so happy about?”
        <br /><br />
        To which Beryl responds, “It just feels like...It feels like Christmas
        morning.”
        <br /><br />
        Catie chuckles. “It is Christmas morning, silly.”
        <br /><br />
        Beryl smiles big. “Oh I know. I know. It is Christmas morning.”
        <br /><br />
        The End
