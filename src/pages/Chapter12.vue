        Offscreen we hear Beryl approaching, again squeezing between the other
        patrons’ legs. He gets to his seat and sits down.
        <br /><br />
        “How’s it coming?” Beryl asks.
        <br /><br />
        “Good, you’re just in time for the Angel.”
        <br /><br />
        We see a myriad of grown ups and children in ornate bath robes with
        shepherd crooks skating around the Bethlehem countryside. They’re
        skating around fake sheep standing on the ice.
        <br /><br />
        The narrator explains, “And there were in the same country shepherds
